<?php 
/**
* Simple PHP Tower Builder Script
*/
function tower_builder(int $n): array {
  $tower = [];
  $usedStars = 0;
  $totalSpaces = ($n * 2) - 1;
  
  for($x = 1; $x <= $n; $x++){
    $t = '';
    $middle = floor($totalSpaces / 2);
	
    if($x == 1){ $usedStars += 1; }else{ $usedStars += 2; }
    
    for($y = 1; $y <= $totalSpaces; $y++){
      if($y == $middle + 1){
        for($vv = 0; $vv < $usedStars; $vv++){ $t .= '*'; } 
      }else{
        $t .= ' ';
      }
    }
    
    $totalSpaces -= 2;
    $tower[$x - 1] = $t;
  }
  
  return $tower;
}

